export class CtsEmployee{
    id:number;
    firstName:string;
    lastName:string;
    salary:number;
    empNo:string;
    constructor(id:number,firstName:string,lastName:string,salary:number,empNo:string){
        this.id=id;
        this.firstName=firstName;
        this.lastName=lastName;
        this.salary=salary;
        this.empNo=empNo;
    }
}